Universitas Islam Negeri (UIN) Syarif Hidayatullah Jakarta
 atau Universitas Islam Negeri (UIN) Jakarta (sebelumnya: IAIN 
 Syarif Hidayatullah atau IAIN Jakarta) adalah sebuah universitas Islam 
 negeri yang terletak di Ciputat, Tangerang Selatan.
<br />
Sejak 2007 UIN Syarif Hidayatullah menetapkan motto "Knowledge, Piety, Integrity". 
Motto ini pertama kali disampaikan Rektor UIN Syarif Hidayatullah Jakarta, 
Prof. Dr. Komaruddin Hidayat, dalam pidato Wisuda Sarjana ke-67 tahun akademik 2006-2007.
<br />
Dalam moto "Knowledge, Piety, Integrity" terkandung sebuah spirit untuk mewujudkan kampus madani, 
sebuah kampus yang berkeadaban, dan menghasilan alumni yang memiliki kedalaman dan keluasaan ilmu, 
ketulusan hati, dan kepribadian kokoh.
